def climb(x, h):
    print(h)
    # keep climbing until we've found a summit
    summit = False

    # edit here
    while not summit:
        summit = True         # stop unless there's a way up
        for x_new in range(max(0, x-5), min(99, x+5)): # check +-5 steps around
            if h[x_new] > h[x]:
                x = x_new
                summit = False
    return x


